    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int coluna = 0;
        int linha = tblBairro.getSelectedRow();

        if (linha >= 0) {
            Long codigo = (Long) tblBairro.getValueAt(linha, coluna);
            bairro = bairroDAO.selecionar(codigo);
            JDialog j = new InserirBairro(null, true, true, bairro);
            j.setLocationRelativeTo(null);
            j.setVisible(true);
            bairro = new Bairro();
            bairroDAO = new BairroDAO();
            //corDAO.remover(cor);
            atualizaTabela("");
        } else {
            JOptionPane.showMessageDialog(this, "Por favor!... Selecione um bairro para alterar!!");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int coluna = 0;
        int linha = tblBairro.getSelectedRow();
        if (linha >= 0) {
            int x = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir esse bairro???", "Excluir...",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (x == JOptionPane.YES_OPTION) {
                Long codigo = (Long) tblBairro.getValueAt(linha, coluna);
                Bairro bairro = bairroDAO.selecionar(codigo);
                bairroDAO.remover(bairro);
                atualizaTabela("");

            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor!... Selecione um bairro para excluir!!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

