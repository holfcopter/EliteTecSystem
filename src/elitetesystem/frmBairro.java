    public void atualizaTabela(String nome) {
        BairroDAO bairroDAO = new BairroDAO();


        if (nome.equals("")) {
            tblBairro.setModel(new BairroTableModel(bairroDAO.listar()));
            tblBairro.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(50);
            tblBairro.getTableHeader().getColumnModel().getColumn(1).setPreferredWidth(450);

        } else {
            tblBairro.setModel(new BairroTableModel(bairroDAO.buscaBairroPorNome(nome)));
            if (nome.equals("")) {
                tblBairro.setModel(new BairroTableModel(bairroDAO.listar()));
                tblBairro.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(50);
                tblBairro.getTableHeader().getColumnModel().getColumn(1).setPreferredWidth(450);

            }
        }
    }

