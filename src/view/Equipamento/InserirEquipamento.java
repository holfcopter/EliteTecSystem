    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!alterar) {
            equipamento = new Equipamento();
        }
        equipamento.setDescricao(txtDescricao.getText());
        equipamento.setCliente(txtCliente.getText());
        equipamento.setNserie(txtNSerie.getText());
        equipamento.setStatus(cmbStatus.getSelectedItem().toString());
        equipamento.setStatusEquipamento(cmbStatusEquipamento.getSelectedItem().toString());
        equipamento.setTipo(cmbTipoEquipamento.getSelectedItem().toString());
        equipamento.setStatusControle("ATIVO");

        boolean retorno = false;
        if (!alterar) {
            retorno = equipamentoDAO.gravar(equipamento);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Equipamento Salvo com Sucesso!!");
                this.dispose();
            }
        } else {
            retorno = equipamentoDAO.gravar(equipamento);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Equipamento alterado com sucesso!!");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

