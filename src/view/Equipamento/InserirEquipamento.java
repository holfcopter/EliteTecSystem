    public InserirEquipamento(java.awt.Frame parent, boolean modal, boolean alterar, final Equipamento equipamento) {
        super(parent, modal);
        initComponents();

        this.alterar = alterar;
        if (alterar) {
            this.equipamento = equipamento;
            txtCliente.setText(equipamento.getCliente());
            txtDescricao.setText(equipamento.getDescricao());
            txtNSerie.setText(equipamento.getNserie());
            try {
                String GetStatus;
                GetStatus = equipamento.getStatus();
                if (GetStatus.equals("Aguardando")) {
                    cmbStatus.setSelectedIndex(1);
                }
                if (GetStatus.equals("Em execução")) {
                    cmbStatus.setSelectedIndex(2);
                }
                if (GetStatus.equals("Concluído")) {
                    cmbStatus.setSelectedIndex(3);
                }
                if (GetStatus.equals("Condenado")) {
                    cmbStatus.setSelectedIndex(4);
                }
                if (GetStatus.equals("Com Defeito")) {
                    cmbStatus.setSelectedIndex(5);
                }
            } catch (Exception e) {
            }
            try {
                String GetStatusEquipamento;
                GetStatusEquipamento = equipamento.getStatusEquipamento();
                if (GetStatusEquipamento.equals("Contrato")) {
                    cmbStatusEquipamento.setSelectedIndex(2);
                }
                if (GetStatusEquipamento.equals("Avulso")) {
                    cmbStatusEquipamento.setSelectedIndex(1);
                }
            } catch (Exception e) {
            }
            try {
                String GetTipo;
                GetTipo = equipamento.getTipo();
                if (GetTipo.equals("Computador")) {
                    cmbTipoEquipamento.setSelectedIndex(1);
                }
                if (GetTipo.equals("Notebook")) {
                    cmbTipoEquipamento.setSelectedIndex(2);
                }
                if (GetTipo.equals("Impressora")) {
                    cmbTipoEquipamento.setSelectedIndex(3);
                }
                if (GetTipo.equals("Tablet")) {
                    cmbTipoEquipamento.setSelectedIndex(4);
                }
                if (GetTipo.equals("NetBook")) {
                    cmbTipoEquipamento.setSelectedIndex(5);
                }
                if (GetTipo.equals("Celular")) {
                    cmbTipoEquipamento.setSelectedIndex(6);
                }
                if (GetTipo.equals("Nobreak")) {
                    cmbTipoEquipamento.setSelectedIndex(7);
                }
                if (GetTipo.equals("Ultrabook")) {
                    cmbTipoEquipamento.setSelectedIndex(8);
                }
                if (GetTipo.equals("AIO")) {
                    cmbTipoEquipamento.setSelectedIndex(9);
                }
            } catch (Exception e) {
            }


        }
    }

